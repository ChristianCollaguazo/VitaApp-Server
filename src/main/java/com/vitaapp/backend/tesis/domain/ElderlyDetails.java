package com.vitaapp.backend.tesis.domain;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.ArrayList;
import java.util.Collection;
import java.util.List;

public class ElderlyDetails implements UserDetails {
    private Elderly elderly;

    public ElderlyDetails(Elderly elderly) {
        this.elderly = elderly;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        List<SimpleGrantedAuthority> authorities = new ArrayList<>();
        authorities.add(new SimpleGrantedAuthority("OLDER"));
        return authorities;
    }

    @Override
    public String getPassword() {
        return elderly.getPassword();
    }

    @Override
    public String getUsername() {
        return elderly.getUsername();
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
}
